export const formatDate = (timestamp: Timestamp) => {
    const date = (timestamp as unknown as FirebaseTimestamp).toDate();
    return date.toLocaleString('id-ID', {
        day: 'numeric',
        month: 'long',
        year: 'numeric',
        hour: '2-digit',
        minute: '2-digit',
        timeZoneName: 'short'
    });
};